Optimal Charging
The fastest way to replenish your Model S is to charge to 80% state of charge, which is enough for travel between many Supercharger stations. Charging the final 20% takes approximately the same amount of time as the first 80% due to a necessary decrease in charging current to help top-off cells. It's somewhat like turning down a faucet in order to fill a glass of water to the top without spilling.</p>
