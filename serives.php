How can I charge at my destination?
Tesla is working with hotels, resorts, and other destinations to encourage the installation of High Power Wall Connectors where our customers spend time away from home. If your organization is interested in offering charging to Tesla owners, send us a note here, or email destinationcharging@teslamotors.com</p>
